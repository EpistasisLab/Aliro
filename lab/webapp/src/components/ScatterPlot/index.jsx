     // if chartKey includes pca, then 
     if (chartKey.includes('pca')) {
      var axis= {
        x: {
            label: 'PC1',
            tick: {
                fit: false
            }
        },
        y: {
            label: 'PC2'
        }
      }
      // else if chartKey includes tsne, then
    } 
    else if (chartKey.includes('tsne')) {
      var axis= {
        x: {
            label: 'Comp 1',
            tick: {
                fit: false
            }
        },
        y: {
            label: 'Comp 2'
        }
      }
    }

    // // make axis tick values like this 30000-> 3 units 10000
